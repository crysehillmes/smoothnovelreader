public class ChapterListItem {
    private String title;
    private String ctsrc;
    private String cid;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getCtsrc() {
        return ctsrc;
    }

    public void setCtsrc(String ctsrc) {
        this.ctsrc = ctsrc;
    }

    public String getCid() {
        return cid;
    }

    public void setCid(String cid) {
        this.cid = cid;
    }
}
