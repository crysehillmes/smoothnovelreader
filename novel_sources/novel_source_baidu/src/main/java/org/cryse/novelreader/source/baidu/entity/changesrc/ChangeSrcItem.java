package org.cryse.novelreader.source.baidu.entity.changesrc;

public class ChangeSrcItem  {
    private String gid;
    private String title;
    private String bookname;
    private String ctsrc;

    public String getGid() {
        return gid;
    }

    public void setGid(String gid) {
        this.gid = gid;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getBookname() {
        return bookname;
    }

    public void setBookname(String bookname) {
        this.bookname = bookname;
    }

    public String getCtsrc() {
        return ctsrc;
    }

    public void setCtsrc(String ctsrc) {
        this.ctsrc = ctsrc;
    }
}
